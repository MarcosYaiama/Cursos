from random import randrange

def jogar():
    imprime_mensagem_abertura()
    palavra_secreta = carrega_palavra_secreta()

    letras_acertadas = ['_' for letra in palavra_secreta]#Modo de usar o for em apenas uma linha, LIST COMPREHENSIONS!!
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while(not enforcou and not acertou):
        chute = pede_chute()
        print(chute)

        if(chute in palavra_secreta):
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if(acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

def imprime_mensagem_abertura():
    print('*********************************')
    print('***Bem vindo ao jogo da Forca!***')
    print('*********************************')

def carrega_palavra_secreta(nome_arquivo='palavras.txt', primeira_linha_valida = 0):#Define o parâmetro opcional caso nenhum seja passado

    palavras = []
    with open(nome_arquivo, "r", encoding="UTF-8") as arquivo:#with é usado para abrir arquivos e deixar o python
        for linha in arquivo:                                   #responsavel por fecha-lo automaticamente caso dê erro no programa
            linha = linha.strip()                               #Pesquisar mais sobre with e a função open()
            palavras.append(linha)

    print(palavras)

    palavra_secreta = palavras[randrange(primeira_linha_valida,len(palavras))].upper()
    arquivo.close()
    return palavra_secreta


def pede_chute():
    chute = input('Qual letra? ').upper().strip()
    return chute

def marca_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1



